package com.akr.vmsapp.mod;

public class Owner {
    private String ownId, username, firstname, lastname, surname, countryCode, phone, email, idNumber,
            gender, dob, photoUrl, lat, lng, locName, status, regDate, password;

    public Owner() {
    }

    public Owner(String ownId, String username, String firstname, String lastname,
                 String surname, String countryCode, String phone, String email, String idNumber,
                 String gender, String dob, String photoUrl, String lat, String lng, String locName,
                 String status, String regDate, String password) {
        this.ownId = ownId;
        this.username = username;
        this.firstname = firstname;
        this.lastname = lastname;
        this.surname = surname;
        this.countryCode = countryCode;
        this.phone = phone;
        this.email = email;
        this.idNumber = idNumber;
        this.gender = gender;
        this.dob = dob;
        this.photoUrl = photoUrl;
        this.lat = lat;
        this.lng = lng;
        this.locName = locName;
        this.status = status;
        this.regDate = regDate;
        this.password = password;
    }

    public String getOwnId() {
        return ownId;
    }

    public void setOwnId(String ownId) {
        this.ownId = ownId;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getCountryCode() {
        return countryCode;
    }

    public void setCountryCode(String countryCode) {
        this.countryCode = countryCode;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getIdNumber() {
        return idNumber;
    }

    public void setIdNumber(String idNumber) {
        this.idNumber = idNumber;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getPhotoUrl() {
        return photoUrl;
    }

    public void setPhotoUrl(String photoUrl) {
        this.photoUrl = photoUrl;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }

    public String getLocName() {
        return locName;
    }

    public void setLocName(String locName) {
        this.locName = locName;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRegDate() {
        return regDate;
    }

    public void setRegDate(String regDate) {
        this.regDate = regDate;
    }
}
